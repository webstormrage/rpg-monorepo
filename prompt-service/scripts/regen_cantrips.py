def fetch(url: str) -> str:
    req = Request(url, headers={"User-Agent": "Mozilla/5.0"})
    with urlopen(req, timeout=20) as response:
        return response.read().decode("utf-8", errors="ignore")


def strip_tags(value: str) -> str:
    return unescape(re.sub(r"<.*?>", "", value)).strip()


def main() -> None:
    index_text = INDEX_PATH.read_text(encoding="utf-8", errors="ignore")
    items = re.findall(
        r'href="(/spells/\d+-([^/]+)/)"[^>]*>.*?cards_list__item-name">(.*?)</span>',
        index_text,
        re.S,
    )

    for href, slug, fallback_name in items:
        url = f"https://dnd.su{href}"
        html = fetch(url)

        title_match = re.search(
            r'<h1 class="center-page_title">\s*<a [^>]+>(.*?)</a>', html, re.S
        )
        title = strip_tags(title_match.group(1)) if title_match else fallback_name

        body_match = re.search(r'<ul class="params card__article-body">(.*?)</ul>', html, re.S)
        body = body_match.group(1) if body_match else ""

        spell_type_match = re.search(r'<li class="size-type-alignment">(.*?)</li>', body, re.S)
        spell_type = strip_tags(spell_type_match.group(1)) if spell_type_match else "Заговор"

        params: dict[str, str] = {}
        for match in re.finditer(r"<li><strong>(.*?)</strong>\s*(.*?)</li>", body, re.S):
            label = strip_tags(match.group(1)).rstrip(":")
            value = strip_tags(match.group(2))
            params[label] = value

        content = [
            f"# {title} [{slug}]",
            "",
            f"- Тип: {spell_type}",
            f"- Время накладывания: {params.get('Время накладывания', '')}",
            f"- Дистанция: {params.get('Дистанция', '')}",
            f"- Компоненты: {params.get('Компоненты', '')}",
            f"- Длительность: {params.get('Длительность', '')}",
            f"- Классы: {params.get('Классы', '')}",
            f"- Источник: {url}",
            "",
            "## Описание",
            "",
            SUMMARIES[slug],
        ]

        if slug in SCALING:
            content.extend(
                [
                    "",
                    "## Масштабирование",
                    "",
                    SCALING[slug],
                ]
            )

        (CANT_DIR / f"{slug}.mdx").write_text("\n".join(content) + "\n", encoding="utf-8")
        print(slug)


if __name__ == "__main__":
    main()
